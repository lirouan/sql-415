DROP TABLE IF EXISTS Ratings;
DROP TABLE IF EXISTS Reviewers;
DROP TABLE IF EXISTS Actors_Movies;
DROP TABLE IF EXISTS Movies;
DROP TABLE IF EXISTS People;

CREATE TABLE Movies(MovieID INT PRIMARY KEY, Title VARCHAR(200), Year INT, Director VARCHAR(200));
CREATE TABLE Reviewers(ReviewerID INT PRIMARY KEY, Name VARCHAR(200));
CREATE TABLE Ratings(ReviewerID INT, MovieID INT, Stars INT, RatingDate DATE,
  PRIMARY KEY(ReviewerID, MovieID, Stars),
  CONSTRAINT FK_rating_movie FOREIGN KEY(MovieID) REFERENCES Movies(MovieID),
  CONSTRAINT FK_rating_reviewer FOREIGN KEY(ReviewerID) REFERENCES Reviewers(ReviewerID));
CREATE TABLE People(PersonID INT PRIMARY KEY, Name VARCHAR(200), BirthYear INT);
CREATE TABLE Actors_Movies(ActorID INT, MovieID INT, Role VARCHAR(200),
  PRIMARY KEY (ActorID, MovieID),
  FOREIGN KEY (ActorID) REFERENCES People(PersonID),
  FOREIGN KEY (MovieID) REFERENCES Movies(MovieID));


